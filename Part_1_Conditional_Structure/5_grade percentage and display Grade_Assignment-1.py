per = float(input("Enter Marks Percentage :"))

if 90 <= per <=100:
        print ("Grade : A+")
elif 80 <= per < 90:
        print ("Grade : A")
elif 70 <= per < 80:
        print ("Grade : B+")
elif 60 <= per < 70:
        print ("Grade : B")
elif 40 <= per < 60:
        print ("Grade : C")
else:
        print ("Grade : F")